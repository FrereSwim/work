package cn.xy.menuBean;

public class DishMenuInfo {

	private String dishID;
	private String name;
	private int num;
	private String dan;
	private String zong;
	//private String 
	public String getDishID() {
		return dishID;
	}
	public void setDishID(String dishID) {
		this.dishID = dishID;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getDan() {
		return dan;
	}
	public void setDan(String dan) {
		this.dan = dan;
	}
	public String getZong() {
		return zong;
	}
	public void setZong(String zong) {
		this.zong = zong;
	}
	
}
