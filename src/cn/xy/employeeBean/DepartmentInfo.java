package cn.xy.employeeBean;

public class DepartmentInfo {
	
	private String id;
	private String dName;
	private String dHead;
	private String dInfo;
	private String dNum;
	private String dMapping;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getdName() {
		return dName;
	}
	public void setdName(String dName) {
		this.dName = dName;
	}
	public String getdHead() {
		return dHead;
	}
	public void setdHead(String dHead) {
		this.dHead = dHead;
	}
	public String getdInfo() {
		return dInfo;
	}
	public void setdInfo(String dInfo) {
		this.dInfo = dInfo;
	}
	public String getdNum() {
		return dNum;
	}
	public void setdNum(String dNum) {
		this.dNum = dNum;
	}
	public String getdMapping() {
		return dMapping;
	}
	public void setdMapping(String dMapping) {
		this.dMapping = dMapping;
	}
	
	
	

}
